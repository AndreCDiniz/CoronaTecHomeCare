CREATE TABLE PROFISSIONAL(
ID_PROFIS NUMBER NOT NULL,
NOME VARCHAR2(255) NOT NULL,
CPF VARCHAR2(22) NOT NULL,
NUM_REGISTRO NUMBER NOT NULL,
TIPO_PROFIS VARCHAR2(255) NOT NULL,
SENHA VARCHAR2(100) NOT NULL,
AREA VARCHAR2(255) NOT NULL,
EMAIL VARCHAR2(100) NOT NULL ,
TELEFONE VARCHAR2(255) NOT NULL,

CONSTRAINT PK_PROFIS PRIMARY KEY(ID_PROFIS) ENABLE
);


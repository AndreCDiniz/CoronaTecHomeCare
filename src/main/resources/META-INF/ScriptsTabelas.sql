ID_PACIENTE NUMBER NOT NULL,
NOME VARCHAR2(255) NOT NULL,
CPF VARCHAR2(22) NOT NULL,
EMAIL VARCHAR2(50) NULL,
TELEFONE VARCHAR2(50) NULL,
SEXO VARCHAR2(10) NOT NULL,
IDADE NUMBER NOT NULL,
PESO NUMBER NOT NULL,
ALTURA NUMBER NOT NULL,
RUA VARCHAR2(255) NOT NULL,
NUMERO VARCHAR2(10) NOT NULL,
BAIRRO VARCHAR2(255) NOT NULL,
CEP VARCHAR2(9) NULL,

CONSTRAINT PK_PACIENTE PRIMARY KEY(ID_PACIENTE)ENABLE
);

CREATE TABLE PROCESSO(
NOME VARCHAR2(255) NOT NULL,
DESCRICAO VARCHAR2(4000) NOT NULL,

CONSTRAINT PK_PROCESSO PRIMARY KEY(NOME) ENABLE
);

CREATE TABLE ATENDIMENTO(
ID_ATENDIMENTO NUMBER NOT NULL,
ID_PROCESSO VARCHAR2(255) NULL,
ID_PACIENTE NUMBER NULL,
ID_PROFIS NUMBER NULL,
DTA DATE NOT NULL,
CID VARCHAR2(50) NOT NULL,
AVALIACAO VARCHAR2(4000) NOT NULL,
EVOLUCAO VARCHAR2(4000) NOT NULL,
PRESCRIAO VARCHAR2(4000) NOT NULL,
OBS VARCHAR2(4000) NULL,
CONDUTA VARCHAR2(4000) NOT NULL,

CONSTRAINT PK_ANTEDIMENTO PRIMARY KEY(ID_ATENDIMENTO) ENABLE
);

